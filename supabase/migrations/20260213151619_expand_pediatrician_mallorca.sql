<p>Mallorca has pediatric specialists covering all major areas:</p>
<ul>
<li><strong>Pediatric allergology:</strong> Essential in Mallorca where pollen allergies are common</li>
<li><strong>Pediatric cardiology:</strong> For heart murmurs and congenital conditions</li>
<li><strong>Pediatric dermatology:</strong> Eczema, birthmarks, and sun-related skin concerns</li>
<li><strong>Pediatric endocrinology:</strong> Growth disorders, thyroid issues, early puberty</li>
<li><strong>Pediatric gastroenterology:</strong> Food intolerances, celiac disease, chronic digestive issues</li>
