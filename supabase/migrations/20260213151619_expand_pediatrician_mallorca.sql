<h2>Pediatric Healthcare in Mallorca: Public vs. Private</h2>
<p>Spain operates a dual healthcare system — public (covered by social security contributions) and private. For pediatric care, both systems provide high-quality treatment, but they differ significantly in access, wait times, language options, and flexibility.</p>

<h3>Public Pediatric Care (Seguridad Social)</h3>
<p>In the public system, every child registered with social security is assigned a pediatrician at their local health center (centro de salud). Key characteristics:</p>
<ul>
<li>Free for all registered residents and their dependents</li>
<li>Assigned pediatrician based on your registered address</li>
<li>Appointments typically available within 1 to 3 days for non-urgent issues</li>
<li>Specialist referrals must go through your assigned pediatrician</li>
<li>Consultations primarily in Spanish and Catalan</li>
<li>Well-child check-ups follow the Balearic Islands health program schedule</li>
<li>Vaccinations included at no additional cost</li>
</ul>
<p>The quality of care in the Spanish public system is excellent — Spain''s healthcare system is ranked 7th globally by the World Health Organization. However, for expat families who do not speak Spanish fluently, the language barrier can be a significant challenge, particularly when discussing symptoms, developmental concerns, or treatment options for your child.</p>

<h3>Private Pediatric Care</h3>
<p>The private system offers greater choice and convenience:</p>
<ul>
<li>Choose your own pediatrician based on language, location, and approach</li>
<li>Same-day or next-day appointments typically available</li>
<li>Longer consultation times (15 to 30 minutes vs. 5 to 10 minutes in public)</li>
<li>Direct access to specialists without GP referral</li>
<li>Multilingual doctors (English, German, French, Swedish common in Calvia area)</li>
<li>Digital appointment booking, teleconsultation, and online medical records</li>
<li>Evening and weekend availability at many practices</li>
</ul>
<p>For families in Calvia, the private route is popular due to the availability of multilingual doctors and the convenience of clinics located throughout the municipality — from Portals Nous and Bendinat to Santa Ponsa and Peguera.</p>

<h2>Types of Pediatric Services Available</h2>

<h3>Well-Child Visits (Revisiones del Nino Sano)</h3>
<p>The Spanish pediatric schedule includes regular check-ups that track growth, development, nutrition, and include the vaccination schedule. The Balearic Islands follow this recommended schedule:</p>
<ul>
<li>Newborn (first week of life)</li>
<li>1 month, 2 months, 4 months, 6 months</li>
<li>9 months (additional in some programs)</li>
<li>12 months, 15 months, 18 months</li>
<li>2 years, 3 years, 4 years</li>
<li>6 years (school entry check)</li>
<li>8, 10, 12, and 14 years</li>
</ul>
<p>Each visit includes measurement of height, weight, and head circumference (for infants), developmental milestone assessment, nutritional guidance, vision and hearing screening at appropriate ages, and discussion of any parental concerns. These visits are essential for early detection of developmental delays, growth issues, and other health concerns.</p>

<h3>Sick Visits and Acute Care</h3>
<p>Most private pediatricians in Calvia offer same-day or next-day appointments for sick children. Common childhood illnesses treated include:</p>
<ul>
<li>Upper respiratory infections (colds, croup, bronchiolitis)</li>
<li>Ear infections (otitis media) — the most common reason for pediatric antibiotic prescriptions</li>
<li>Gastroenteritis (stomach flu) — particularly common in summer months</li>
<li>Hand, foot, and mouth disease</li>
<li>Skin conditions (eczema, impetigo, fungal infections)</li>
<li>Allergic reactions (including food allergies and insect stings)</li>
<li>Febrile seizures (fever-related convulsions, which can be frightening but are usually benign)</li>
<li>Urinary tract infections</li>
</ul>
<p>Many clinics also offer telephone or video consultations for minor concerns, which can be particularly useful for assessing whether a clinic visit is necessary — saving time and reducing exposure to other sick children in waiting rooms.</p>

<h3>Emergency Pediatric Care</h3>
<p>For serious situations, know these emergency resources in advance:</p>
<ul>
<li><strong>112:</strong> Spain''s universal emergency number (equivalent to 911 or 999). Operators speak multiple languages.</li>
<li><strong>Hospital Universitari Son Espases:</strong> Mallorca''s main public hospital has a dedicated pediatric emergency department staffed 24/7. This is the reference hospital for serious pediatric emergencies.</li>
<li><strong>Hospital Juaneda Miramar:</strong> Private hospital with pediatric emergency services.</li>
<li><strong>Clinica Rotger:</strong> Private facility with emergency department.</li>
</ul>
<p>For non-life-threatening after-hours concerns, the PAC (Punto de Atencion Continuada) urgent care centers in Calvia provide evening, night, and weekend coverage. Your centro de salud can direct you to the nearest PAC.</p>

