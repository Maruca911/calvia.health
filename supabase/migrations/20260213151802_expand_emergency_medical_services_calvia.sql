<p>Knowing how to access <strong>emergency medical services in Calvia</strong> can make a critical difference when every minute counts. Whether you are a long-term resident, a seasonal visitor, or a tourist, understanding Mallorca''s emergency healthcare infrastructure — hospitals, urgent care centers, ambulance services, and pharmacies — ensures you can act quickly and confidently in a medical crisis.</p>

<p>This guide covers every aspect of emergency healthcare in the Calvia region and across Mallorca, including the key phone numbers, hospital locations, what to expect at a Spanish emergency department, costs for uninsured visitors, and practical advice for specific emergency scenarios.</p>

<h2>Essential Emergency Numbers</h2>
<p>Save these numbers in your phone before you need them:</p>
<ul>
<li><strong>112</strong> — Universal European emergency number (police, fire, ambulance). Multilingual operators available 24/7. This is your first call in any life-threatening emergency.</li>
<li><strong>061</strong> — SAMU (Servei d''Atencion Medica Urgent) — Balearic Islands medical emergency service. Use this for medical emergencies specifically. Operators can dispatch ambulances and provide telephone medical guidance.</li>
<li><strong>091</strong> — National Police</li>
<li><strong>092</strong> — Local Police (Policia Local Calvia)</li>
<li><strong>080</strong> — Fire service (Bomberos)</li>
</ul>
<p>When calling 112 or 061, be prepared to give: your location (as precisely as possible — address, nearby landmarks, beach name), the nature of the emergency, the number and age of people affected, and your phone number. If you do not speak Spanish, say "English please" — multilingual operators are available, though there may be a brief transfer.</p>

<h2>Hospitals Serving the Calvia Region</h2>

<h3>Hospital Universitari Son Espases (Public)</h3>
<p>Mallorca''s main public hospital and the reference center for serious emergencies. Located on the Palma ring road (approximately 15 to 25 minutes from most Calvia areas by car), Son Espases is a large university hospital with:</p>
<ul>
<li>24/7 emergency department (urgencias) with separate adult and pediatric sections</li>
<li>All medical and surgical specialties</li>
<li>Intensive care units (adult, pediatric, and neonatal)</li>
<li>Trauma center and helicopter landing pad</li>
<li>Advanced diagnostic imaging (CT, MRI, interventional radiology)</li>
<li>Cardiac catheterization lab for heart attack treatment</li>
<li>Stroke unit with 24/7 thrombolysis capability</li>
</ul>
<p>Emergency treatment at Son Espases is free for all EU citizens with a European Health Insurance Card (EHIC) or Spanish health card (TSI). Non-EU visitors may be charged but will always be treated first — billing comes later.</p>

<h3>Hospital Juaneda Miramar (Private)</h3>
<p>A well-equipped private hospital in Palma (approximately 20 minutes from central Calvia). Features include:</p>
<ul>
<li>24/7 emergency department with shorter average wait times than public hospitals</li>
<li>Full range of specialties including cardiology, orthopedics, neurology, and general surgery</li>
<li>Private rooms as standard for admitted patients</li>
<li>Multilingual staff (English, German, French widely spoken)</li>
<li>Direct billing with most Spanish and international insurance companies</li>
</ul>
<p>If you have Spanish private insurance (Sanitas, Asisa, Adeslas, DKV) or international coverage (Cigna Global, Bupa International), confirm that Juaneda Miramar is in your network before visiting. Most major insurers cover this hospital.</p>

<h3>Clinica Rotger (Private)</h3>
<p>Another well-regarded private hospital in central Palma, known for orthopedic and surgical excellence. Features 24/7 emergency services and comprehensive specialist coverage. Particularly popular with German-speaking patients due to long-established relationships with German insurers and German-speaking medical staff.</p>

<h3>Hospital de Son Llatzer (Public)</h3>
<p>The second public hospital in Palma, located in the eastern part of the city. Provides emergency services, general medicine, and most specialties. Can be an alternative to Son Espases when the main hospital has high volumes.</p>

<h2>Urgent Care Centers (PAC and Health Centers)</h2>
<p>For medical issues that need prompt attention but are not life-threatening, Calvia''s urgent care infrastructure provides closer and faster options than hospital emergency departments:</p>

<h3>PAC Calvia (Punto de Atencion Continuada)</h3>
<p>The PAC urgent care center in Calvia provides medical coverage outside normal health center hours — evenings, nights, weekends, and public holidays. Services include assessment and treatment of non-emergency conditions, wound care, fever management, ear and throat infections, urinary infections, minor injuries, prescription of urgent medications, and referral to hospital when necessary. The PAC is staffed by qualified doctors (medicos de urgencias) and nurses.</p>

<h3>Health Centers (Centros de Salud)</h3>
<p>During business hours (typically 8:00 to 15:00, some until 20:00), your local public health center in Calvia provides same-day urgent appointments for acute but non-emergency issues. Each village in the Calvia municipality has a health center or consulting room (consultorio) served by rotating medical staff.</p>

<h2>Ambulance Services in Mallorca</h2>
<p>Ambulance services are coordinated through the 061 emergency medical dispatch center:</p>
<ul>
<li><strong>BLS (Basic Life Support) ambulances:</strong> Staffed by emergency medical technicians. Used for urgent but non-critical transfers.</li>
<li><strong>ALS (Advanced Life Support) ambulances:</strong> Staffed by a doctor, nurse, and EMT. Equipped for cardiac monitoring, defibrillation, intubation, and IV medication. Dispatched for serious emergencies including suspected heart attacks, strokes, severe trauma, and respiratory distress.</li>
<li><strong>Helicopter (HEMS):</strong> The 061 operates a medical helicopter for emergencies in remote areas, serious road traffic accidents, and rapid transfer to Son Espases from anywhere on the island. Response time is typically under 15 minutes to any location in Mallorca.</li>
</ul>

<h3>Ambulance Response Times</h3>
<p>Average response times in the Calvia area for emergency (priority 1) calls are approximately 8 to 15 minutes, depending on the time of day and exact location. Response times are faster in urban areas (Palmanova, Magaluf, Santa Ponsa) and may be slightly longer in more rural areas (Calvia village, Es Capdella). During summer months, increased population and traffic can extend response times.</p>

<h2>What to Expect at a Spanish Emergency Department</h2>
<p>Understanding the process reduces stress during an already stressful situation:</p>

<h3>Triage</h3>
<p>Upon arrival, a triage nurse assesses the urgency of your condition using a 5-level system (similar to the Manchester Triage System used across Europe):</p>
<ol>
<li><strong>Level 1 (Resuscitation):</strong> Life-threatening — immediate treatment</li>
<li><strong>Level 2 (Emergent):</strong> Very urgent — seen within 10 minutes</li>
<li><strong>Level 3 (Urgent):</strong> Seen within 60 minutes</li>
<li><strong>Level 4 (Less Urgent):</strong> Seen within 120 minutes</li>
<li><strong>Level 5 (Non-Urgent):</strong> Seen within 240 minutes</li>
</ol>
<p>This means that patients with minor injuries may wait several hours if the department is busy with more serious cases. This is not neglect — it is a prioritization system designed to save lives. Bring patience, a phone charger, and water.</p>

<h3>What to Bring to the Emergency Department</h3>
<ul>
<li>Identification (passport, NIE, or Spanish ID)</li>
<li>Health insurance card (TSI for public, or private insurance card)</li>
<li>European Health Insurance Card (EHIC) if visiting from another EU country</li>
<li>A list of current medications, allergies, and relevant medical conditions</li>
<li>Phone and charger</li>
<li>Water and a light snack (waits can be long for non-urgent cases)</li>
</ul>

<h2>Emergency Costs for Visitors Without Insurance</h2>
<p>Spain will never refuse emergency treatment based on insurance status or ability to pay. However, costs can be significant for uninsured visitors:</p>
<ul>
<li>Emergency department visit and assessment: 200 to 500 euros</li>
<li>Blood tests: 50 to 200 euros</li>
<li>X-ray: 50 to 150 euros</li>
<li>CT scan: 200 to 600 euros</li>
<li>Ambulance transport: 200 to 800 euros depending on type and distance</li>
<li>Hospital admission (per night): 400 to 1,200 euros in a public hospital</li>
<li>Surgery: costs vary dramatically — from 1,000 euros for a simple procedure to tens of thousands for complex surgery</li>
</ul>

<p><strong>For EU citizens:</strong> Present your European Health Insurance Card (EHIC) or UK Global Health Insurance Card (GHIC) for free or reduced-cost treatment at public facilities. Private hospitals are not covered by EHIC.</p>
<p><strong>For non-EU visitors:</strong> Travel insurance is strongly recommended. Without it, you will receive a bill directly from the hospital. Some hospitals accept payment plans. Consult your embassy or consulate for assistance if needed.</p>

<h2>Specific Emergency Scenarios</h2>

<h3>Heart Attack (Suspected)</h3>
<p>Symptoms: chest pain or pressure, pain radiating to the arm, jaw, or back, shortness of breath, cold sweats, nausea. Call 112 immediately. Chew an aspirin (300mg) if available and not allergic. Do not drive yourself — wait for the ambulance. Time is critical: the 061 system in Mallorca can activate the cardiac catheterization lab at Son Espases en route, minimizing the time from symptom onset to treatment.</p>

<h3>Stroke (Suspected)</h3>
<p>Use the FAST test: Face drooping, Arm weakness, Speech difficulty, Time to call 112. Stroke treatment is extremely time-sensitive — the clot-dissolving medication (thrombolysis) is most effective within 4.5 hours of symptom onset, and every minute of delay results in the loss of approximately 1.9 million neurons, according to the American Heart Association. Son Espases has a 24/7 stroke unit.</p>

<h3>Allergic Reaction (Anaphylaxis)</h3>
<p>Symptoms: swelling of the throat, difficulty breathing, widespread hives, dizziness, rapid pulse. Use an EpiPen (auto-injector adrenaline) if available. Call 112. Anaphylaxis can be fatal within minutes without treatment. Common triggers in Mallorca include wasp and bee stings, shellfish, nuts, and processionary pine caterpillar contact (January to April).</p>

<h3>Drowning or Near-Drowning</h3>
<p>Call 112 immediately. Begin CPR if the person is not breathing. Mallorca''s beaches with lifeguard coverage (bandera roja/amarilla/verde flag system) provide immediate first response. Off-season or at non-guarded beaches, bystander CPR saves lives — consider taking a course through the Red Cross in Palma.</p>

<h3>Dental Emergencies</h3>
<p>For severe toothache, knocked-out teeth, or dental trauma: during business hours, contact your regular dentist or a dental emergency service. Outside hours, hospital emergency departments can manage dental pain and prescribe antibiotics for dental infections, but they generally do not have dentists on staff for definitive dental treatment. Keep a knocked-out permanent tooth moist (in milk or saliva) and seek dental care within 30 minutes for the best chance of reimplantation.</p>

<h3>Mental Health Crisis</h3>
<p>If someone is in acute mental health distress, suicidal crisis, or experiencing a psychotic episode, call 112. The 024 suicide prevention helpline (Telefono de Atencion a la Conducta Suicida) provides 24/7 support. Hospital Son Espases has a psychiatric emergency service. For non-emergency mental health support, your GP or private psychiatrist can arrange same-day or next-day consultations.</p>

<h3>Poison or Toxic Ingestion</h3>
<p>Call the Spanish National Toxicology Service: 91 562 04 20 (24/7). Provide the substance name, amount ingested, time of ingestion, and the patient''s age and weight. Do not induce vomiting unless specifically instructed by the poison control center. Call 112 if the person is unconscious, having seizures, or having difficulty breathing.</p>

<h2>Pharmacies and Medication Access</h2>
<p>Spanish pharmacies (farmacias) are often the first point of contact for minor health issues and are an excellent resource:</p>
<ul>
<li>Pharmacists in Spain hold university degrees and can assess, advise on, and dispense treatments for many common conditions without a doctor''s visit</li>
<li>Many over-the-counter medications that require prescriptions in other countries are available directly from Spanish pharmacies</li>
<li>Every area in Calvia has pharmacies open during regular business hours</li>
<li>A duty pharmacy (farmacia de guardia) is open 24/7, including nights, weekends, and holidays. The rota is posted on the door of every pharmacy and available online at the Colegio de Farmaceuticos de Baleares website</li>
</ul>

