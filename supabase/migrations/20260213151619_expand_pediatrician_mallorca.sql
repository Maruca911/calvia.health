<li><strong>Pediatric speech therapy (logopedia):</strong> Language delays, bilingual development concerns, articulation issues</li>
</ul>
<p>In the private system, you can access specialists directly. In the public system, referrals come through your assigned pediatrician. Wait times for public specialist appointments range from 2 weeks to several months depending on specialty and urgency.</p>

<h2>Choosing the Right Pediatrician in Calvia</h2>
<p>Finding the right match for your family involves several important considerations:</p>
<ul>
<li><strong>Language:</strong> Ensure the pediatrician speaks your preferred language well enough to discuss medical concepts, developmental concerns, and treatment options in detail. In the Calvia area, many pediatricians speak English, German, and French in addition to Spanish and Catalan. Test the language capability during your first consultation.</li>
<li><strong>Location and accessibility:</strong> Consider proximity to your home and your children''s school. Also verify parking availability and public transport access. Check evening and weekend availability for those inevitable after-hours illnesses.</li>
<li><strong>Philosophy of care:</strong> Some pediatricians favor a conservative approach (watchful waiting, fewer antibiotics, emphasis on natural recovery), while others are more interventionist. Neither is wrong — it depends on your family''s values and comfort level. Discuss the doctor''s approach during your first visit.</li>
<li><strong>Hospital affiliations:</strong> Know which hospitals your pediatrician works with and has admitting privileges at. If your child needs hospitalization, your familiar doctor can continue to manage care.</li>
<li><strong>Clinic environment:</strong> Visit the clinic before your first appointment if possible. Is the waiting room child-friendly? Is it separated from sick and well-child areas? Are the facilities clean and welcoming?</li>
<li><strong>Digital capabilities:</strong> Can you book appointments online? Does the clinic offer teleconsultation? Can you access your child''s medical records digitally? These features add significant convenience for busy families.</li>
<li><strong>Parent community recommendations:</strong> The expat community in Calvia is well connected. Parent groups on social media, WhatsApp groups, and communities at international schools are excellent sources of genuine, tested recommendations.</li>
</ul>

<h2>Vaccination in Mallorca</h2>
<p>Spain has a comprehensive childhood vaccination schedule that is largely aligned with WHO and European Centre for Disease Prevention and Control (ECDC) recommendations. The Balearic Islands follow the national schedule with some regional additions.</p>

<h3>The Spanish Vaccination Schedule (2024-2025)</h3>
<ul>
<li><strong>Birth:</strong> Hepatitis B (first dose)</li>
<li><strong>2 months:</strong> DTPa-IPV-Hib-HepB (hexavalent), Pneumococcal (PCV13), Rotavirus (oral), Meningococcal B</li>
<li><strong>4 months:</strong> DTPa-IPV-Hib-HepB, Pneumococcal, Rotavirus, Meningococcal B</li>
<li><strong>6 months:</strong> Meningococcal B (third dose)</li>
<li><strong>11 months:</strong> DTPa-IPV-Hib-HepB (booster), Pneumococcal (booster)</li>
<li><strong>12 months:</strong> MMR (measles, mumps, rubella), Meningococcal C, Meningococcal B (booster)</li>
<li><strong>15 months:</strong> Varicella (chickenpox, first dose)</li>
<li><strong>3-4 years:</strong> MMR (second dose), Varicella (second dose)</li>
<li><strong>6 years:</strong> DTPa-IPV booster</li>
<li><strong>12 years:</strong> HPV (human papillomavirus, for both boys and girls), Meningococcal ACWY, Tdap booster</li>
<li><strong>14 years:</strong> Meningococcal ACWY (if not given at 12)</li>
</ul>

<h3>Transferring Vaccination Records</h3>
<p>If your child has been vaccinated in another country, bring their vaccination records (ideally translated into Spanish if not in English). Your pediatrician will review them and integrate your child into the Spanish schedule, adding any missing vaccines. Spain''s schedule is broadly similar to those in the UK, Germany, and other EU countries, but there may be differences in timing or specific vaccines used. For example, the UK does not routinely vaccinate against Meningococcal B at the same schedule, and some countries do not include rotavirus in their standard program.</p>

<p>The WHO recommends that children who move between countries should have their vaccination status reviewed by a local healthcare professional as soon as possible to identify and fill any gaps.</p>

<h2>Common Childhood Health Concerns in Mallorca</h2>

<h3>Sun Protection for Children</h3>
<p>Mallorca''s strong Mediterranean sun requires diligent protection. The Spanish Association of Pediatrics (AEP) and the WHO recommend:</p>
<ul>
<li>SPF 50+ broad-spectrum sunscreen for all children over 6 months, reapplied every 2 hours and after swimming</li>
<li>Infants under 6 months should be kept out of direct sunlight entirely</li>
<li>Protective clothing including wide-brimmed hats and UV-protective swimwear</li>
<li>Avoid direct sun exposure between 12:00 and 16:00 during summer months (UV index regularly exceeds 9 in Mallorca)</li>
<li>Quality UV-blocking sunglasses appropriate for children</li>
</ul>
<p>Sunburn in childhood significantly increases the risk of skin cancer later in life. The Skin Cancer Foundation notes that even one blistering sunburn in childhood more than doubles the risk of melanoma.</p>

<h3>Allergies in Mallorca</h3>
<p>Mallorca''s diverse plant life means allergies are common among children. Key allergens by season:</p>
<ul>
<li><strong>February to April:</strong> Cypress and parietaria (wall pellitory) — parietaria is year-round in mild winters</li>
<li><strong>March to June:</strong> Olive tree pollen — the primary allergen in the Balearic Islands</li>
<li><strong>April to July:</strong> Grass pollen</li>
<li><strong>Summer:</strong> Dust mites increase with humidity</li>
<li><strong>Year-round:</strong> Mold spores, pet dander</li>
</ul>
<p>If your child develops persistent nasal congestion, sneezing, itchy eyes, or wheezing, your pediatrician can arrange allergy testing (skin prick tests or blood tests) and recommend treatment including antihistamines, nasal corticosteroids, or immunotherapy (allergy shots or sublingual drops).</p>

<h3>Water Safety</h3>
<p>With swimming pools and beaches being central to Mallorcan life, water safety is paramount. Drowning is one of the leading causes of accidental death in children aged 1 to 4, according to the WHO. Your pediatrician can advise on:</p>
<ul>
<li>Age-appropriate swimming instruction (recommended from age 4, though water familiarization can start earlier)</li>
<li>Pool fencing requirements (mandatory for private pools in Spain under certain regulations)</li>
<li>Beach safety — understanding flag systems and rip currents</li>
<li>Never leaving children unsupervised near water, even for a moment</li>
<li>CPR training for parents (courses available through the Red Cross in Palma)</li>
</ul>

<h3>Insect Bites and Stings</h3>
<p>Tiger mosquitoes (Aedes albopictus) are present in Mallorca and bite during daylight hours. Processionary pine caterpillars (January to April) are a serious health hazard — their tiny hairs cause severe allergic reactions in children and pets. Your pediatrician can advise on prevention and treatment.</p>

<h3>Bilingual Language Development</h3>
<p>Many expat children in Mallorca grow up in multilingual environments (English at home, Spanish and Catalan at school, perhaps other languages with friends). This is entirely normal and beneficial for cognitive development. However, some parents worry about perceived language delays. Research consistently shows that bilingual children may take slightly longer to reach language milestones but ultimately have richer vocabularies and better cognitive flexibility. If you have concerns, a pediatric speech therapist (logopeda) experienced with bilingual children can provide assessment and reassurance or intervention if needed.</p>

<h2>Child Mental Health Services in Mallorca</h2>
<p>The transition to life in a new country can be challenging for children. Common issues include adjustment difficulties, separation anxiety, social integration struggles, and grief for friendships left behind. Mallorca has several English-speaking child psychologists and therapists experienced with expat family issues.</p>

<p>Signs that your child might benefit from professional support include persistent sadness, withdrawal from activities they previously enjoyed, changes in eating or sleeping patterns, declining school performance, excessive worry, and behavioral changes. Your pediatrician can provide referrals to appropriate specialists.</p>

<h2>What to Expect at Your First Pediatric Visit</h2>
<ol>
<li><strong>Medical history review:</strong> Bring all previous medical records, vaccination certificates, growth charts, and any relevant test results from your home country. Having these translated into Spanish or English (if originally in another language) speeds up the process.</li>
<li><strong>Physical examination:</strong> A thorough check-up including height, weight, head circumference (for infants), blood pressure (from age 3), vision screening, hearing assessment, and developmental evaluation appropriate to your child''s age.</li>
<li><strong>Discussion of concerns:</strong> Your opportunity to discuss any health concerns, developmental questions, dietary issues, sleep problems, behavioral observations, or school-related health needs.</li>
<li><strong>Care plan establishment:</strong> Your pediatrician will outline a care plan including upcoming vaccinations, recommended screenings, and the schedule for future visits.</li>
<li><strong>Emergency protocols:</strong> Establish clear guidelines for when to call the clinic, when to go to urgent care, and when to go directly to the emergency department.</li>
</ol>

