<li>Consider a first aid and CPR course (Cruz Roja Palma offers courses in multiple languages)</li>
<li>Ensure your insurance details are easily accessible (app on phone, card in wallet)</li>
<li>If you have a chronic condition, wear a medical alert bracelet or carry a medical information card</li>
<li>Know the duty pharmacy schedule for your area</li>
</ol>

<h2>Frequently Asked Questions</h2>

<h3>Will I be treated in an emergency if I don''t have insurance?</h3>
<p>Yes, absolutely. Spanish law requires emergency departments to treat all patients regardless of insurance status, nationality, or ability to pay. You will receive a bill afterward, but treatment is never withheld in an emergency.</p>

<h3>How much does an ambulance cost in Mallorca?</h3>
<p>For residents with a Spanish health card (TSI) or EU citizens with EHIC, public ambulance services dispatched through 112/061 are free. For uninsured individuals, ambulance costs range from 200 to 800 euros. Private ambulance services used for non-emergency transfers are always charged separately.</p>

<h3>Can I use my UK GHIC card in Mallorca?</h3>
<p>Yes. The UK Global Health Insurance Card (GHIC, which replaced EHIC for UK residents post-Brexit) entitles you to medically necessary treatment at Spanish public healthcare facilities on the same terms as Spanish residents. It does not cover private hospitals or planned medical tourism. Apply for a GHIC free of charge through the NHS website before traveling.</p>

<h3>What if I need emergency care on the beach?</h3>
<p>Mallorca''s main beaches have lifeguard services during summer months (typically June to September). Lifeguards are trained in first aid and can call emergency services. Red flag means no swimming; yellow flag means caution; green flag means safe conditions. On unguarded beaches, call 112 and provide the beach name and your location as precisely as possible.</p>

<h3>Is there a children''s emergency department in Mallorca?</h3>
<p>Yes. Hospital Universitari Son Espases has a separate pediatric emergency department (urgencias pediatricas) staffed by pediatric emergency specialists 24/7. This is the appropriate destination for serious childhood emergencies. For non-urgent pediatric issues outside business hours, the PAC in Calvia can assess and treat most common childhood ailments.</p>

