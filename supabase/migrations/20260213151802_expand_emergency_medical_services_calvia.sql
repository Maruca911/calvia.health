<ol>
<li>Save 112 and 061 in your phone contacts as favorites</li>
<li>Know your home address in Spanish (street name, number, floor, village name) to give to emergency operators</li>
<li>Keep a card in your wallet listing: blood type, allergies, current medications, emergency contact, insurance policy number</li>
