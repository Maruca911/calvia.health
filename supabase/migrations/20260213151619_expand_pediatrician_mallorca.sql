<h2>School Health Requirements in Mallorca</h2>
<p>Spanish schools (both public and private/international) require:</p>
<ul>
<li><strong>Vaccination record (cartilla de vacunacion):</strong> Required at enrollment. While vaccination is not legally mandatory in Spain, schools require documentation of vaccination status.</li>
<li><strong>Allergy and medical condition documentation:</strong> Schools need to know about any allergies (food, medication, insect stings), chronic conditions (asthma, diabetes, epilepsy), and emergency medication requirements.</li>
<li><strong>Emergency action plans:</strong> For children with conditions like severe allergies (requiring EpiPen) or asthma, a written action plan from your pediatrician should be provided to the school.</li>
<li><strong>International schools:</strong> May have additional requirements including a full medical examination report, dental check documentation, and specific form formats from your pediatrician.</li>
</ul>

<h2>Frequently Asked Questions</h2>

<h3>Can I find an English-speaking pediatrician in Calvia?</h3>
<p>Yes. The Calvia area, particularly Santa Ponsa, Portals Nous, and Bendinat, has several pediatric practices with English-speaking doctors. International hospitals in Palma also offer multilingual pediatric care. When booking, confirm the specific doctor (not just the reception staff) speaks your language.</p>

<h3>Do I need to register with the public health system?</h3>
<p>If you are a legal resident paying into social security (or a dependent of someone who is), you are entitled to free public pediatric care. Many families use both systems — public for routine care and vaccinations (free), and private for convenience, specialist access, and English-speaking consultations.</p>

<h3>What about house calls for sick children?</h3>
<p>Several private pediatricians in Calvia offer home visits, which is particularly useful for very young children, high fevers, or when multiple siblings are ill simultaneously. Costs are higher than office visits (100 to 200 euros) but many parents find the convenience invaluable — especially avoiding waiting rooms full of other sick children.</p>

<h3>How do school health requirements work in Mallorca?</h3>
<p>Spanish schools require proof of vaccination (cartilla de vacunacion) at enrollment. International schools may have additional health form requirements, medical examination reports, and specific immunization documentation. Your pediatrician can prepare all necessary paperwork, often within 1 to 2 appointments.</p>

<h3>Is there a pediatric dentist in the Calvia area?</h3>
<p>Yes, several dental clinics in Calvia offer pediatric dentistry (odontopediatria) with dentists trained to work with children. The PADI program provides free basic dental care for children aged 6 to 15 through the public system. For more information, see our guide to <a href="/blog/best-dentist-mallorca">finding a dentist in Mallorca</a>.</p>

<h3>What if my child needs hospitalization?</h3>
<p>Hospital Universitari Son Espases has a dedicated pediatric ward and pediatric ICU. Private hospitals including Juaneda Miramar also provide pediatric inpatient care. If your private pediatrician has admitting privileges, they can continue managing your child''s care during hospitalization. For children with private insurance, a parent can typically stay overnight in the child''s room at no additional cost.</p>

<h3>How do I get prescriptions for my child in Spain?</h3>
<p>Your pediatrician issues a prescription (receta) which you take to any pharmacy (farmacia). Spanish pharmacies are highly accessible — every town in Calvia has at least one, and there is always a pharmacy on duty (farmacia de guardia) for nights, weekends, and holidays. Many common pediatric medications are available over the counter (fever reducers, cough medicine, antihistamines), and pharmacists in Spain are highly trained and can provide advice on minor childhood ailments.</p>

